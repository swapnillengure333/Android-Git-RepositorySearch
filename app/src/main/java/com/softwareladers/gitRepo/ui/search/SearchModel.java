package com.softwareladers.gitRepo.ui.search;

public class SearchModel {
    private String repo_name;
    private String repo_full_name;
    private String repo_image;
    private String repo_watch;


    public String getRepo_name() {
        return repo_name;
    }
    public void setRepo_name(String repo_name) {
        this.repo_name = repo_name;
    }

    public String getRepo_full_name() {
        return repo_full_name;
    }
    public void setRepo_full_name(String repo_full_name) {
        this.repo_full_name = repo_full_name;
    }

    public String getRepo_image() {
        return repo_image;
    }
    public void setRepo_image(String repo_image) {
        this.repo_image = repo_image;
    }

    public String getRepo_watch() {
        return repo_watch;
    }
    public void setRepo_watch(String repo_watch) {
        this.repo_watch = repo_watch;
    }
}
